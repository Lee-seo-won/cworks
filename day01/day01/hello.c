#include <stdio.h>
//전처리기 (미리 처리) : 컴파일 수행전에 먼저 처리
//슬래시 두개 치면 보이지 않음 (주석)
// 도구 - 옵션 글꼴 변경하면 consolas로 변경가능 

int main() {  //메인 함수(프로그램 시작점)

	printf("Hello~ C!\n");  //stdio.h에서 연결함
	printf("안녕~C!\n");

	//숫자4를 출력 (그냥 "4"는 문자)
	printf("정수 : %d\n", 4+10 ); //printf("출력형식", 출력데이터 %d는 정수 출력)  
	printf("실수 : %.2f\n", 2.54); //실수 2.54를 출력 %f는 실수 .2는 소수점 둘째자리
	printf("문자 : %c\n", 'k'); //한개 문자 - 홑따옴표

	return 0; //운영체제로 0을 반환해서 프로그램을 종료한다.
}
